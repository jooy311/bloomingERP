package com.itwill.dto;

/*이름              널?       유형            
--------------- -------- ------------- 
ORDER_NO        NOT NULL VARCHAR2(50)  
ORDER_MEMBER_NO NOT NULL VARCHAR2(50)  
ORDER_STATUS    NOT NULL NUMBER        
ORDER_CNT       NOT NULL NUMBER        
ORDER_DATE      NOT NULL VARCHAR2(10)  
ORDER_PRODUCT   NOT NULL VARCHAR2(100) */

public class Order {
	private String orderNo;
	private String orderMemberNo;
	private int orderStatus;
	private int orderCnt;
	private String orderDate;
	private String orderProduct;

	private Member member;

	public Member getMember() {
		return member;
	}

	public void setMember(Member member) {
		this.member = member;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getOrderMemberNo() {
		return orderMemberNo;
	}

	public void setOrderMemberNo(String orderMemberNo) {
		this.orderMemberNo = orderMemberNo;
	}

	public int getOrderStatus() {
		return orderStatus;
	}

	public void setOrderStatus(int orderStatus) {
		this.orderStatus = orderStatus;
	}

	public int getOrderCnt() {
		return orderCnt;
	}

	public void setOrderCnt(int orderCnt) {
		this.orderCnt = orderCnt;
	}

	public String getOrderDate() {
		return orderDate;
	}

	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}

	public String getOrderProduct() {
		return orderProduct;
	}

	public void setOrderProduct(String orderProduct) {
		this.orderProduct = orderProduct;
	}
	

}
